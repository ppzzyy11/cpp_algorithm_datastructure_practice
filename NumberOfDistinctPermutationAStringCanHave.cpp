int NumberOfDistinctPermutationAStringCanHave(const string& str)
{
	vector<int> rec(256,0);
	for(auto s:str)
	{
		rec[s]++;
	}

	int num=1;
	for(int i=1;i<=str.size();i++)
	{
		num*=i;
	}

	for(int i=0;i<rec.size();i++)
	{
		int k=rec[i];
		while(k!=0)
		{
			num=num/k;
			k--;
		}
	}
	return num;
}

